    public ImageIcon ResizeImage(String impath) {

        ImageIcon myImage = new ImageIcon(impath);
        Image img = myImage.getImage();
        Image newImage = img.getScaledInstance(140, 160, Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImage);
        return image;
    }

    
    public ImageIcon iconsize(String impath) {

        ImageIcon myImage = new ImageIcon(impath);
        Image img = myImage.getImage();
        Image newImage = img.getScaledInstance(100,120, Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImage);
        return image;
    }
    
    
